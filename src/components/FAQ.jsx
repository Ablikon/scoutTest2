import { useState } from 'react'
import './FAQ.css'
import { IoAdd, IoRemove } from 'react-icons/io5'

function FAQ() {
  const [openIndex, setOpenIndex] = useState(null)

  const items = [
    {
      q: "Зачем вам мой логин и пароль от Kaspi?",
      a: "Чтобы бот мог менять цены, он должен авторизоваться как менеджер. Это безопасно: мы не просим доступ к банковским счетам и ЭЦП. Вы можете создать отдельного «Электронного менеджера» с ограниченными правами."
    },
    {
      q: "Это законно? Меня не заблокируют?",
      a: "Это абсолютно законно. Бот делает то же самое, что и человек, просто быстрее. Мы не взламываем сайт Kaspi, а работаем через официальный кабинет продавца."
    },
    {
      q: "Что если конкурент тоже использует бота?",
      a: "Вы устанавливаете минимальную цену (себестоимость), ниже которой падать нельзя. Бот будет снижать цену до этого порога. Если конкурент упадет ниже — бот остановится."
    },
    {
      q: "Можно ли управлять несколькими магазинами?",
      a: "Да, в одном кабинете TopPrice можно добавить несколько магазинов. При подключении второго магазина мы даем скидку 20%."
    },
    {
      q: "Как часто обновляются цены?",
      a: "Стандартная скорость — проверка каждые 3 минуты. Этого достаточно, чтобы опережать 99% конкурентов."
    },
    {
      q: "Как попробовать бесплатно?",
      a: "Мы даем 3 дня бесплатного доступа ко всему функционалу. Карту привязывать не нужно. Просто регистрируетесь и тестируете."
    }
  ]

  const toggle = (index) => {
    setOpenIndex(openIndex === index ? null : index)
  }

  return (
    <section className="faq">
      <div className="container">
        <h2>Часто задаваемые вопросы</h2>
        <div className="faq-accordion">
          {items.map((item, index) => (
            <div key={index} className={`faq-row ${openIndex === index ? 'open' : ''}`}>
              <div className="faq-head" onClick={() => toggle(index)}>
                <h4>{item.q}</h4>
                <div className="faq-icon">
                  {openIndex === index ? <IoRemove /> : <IoAdd />}
                </div>
              </div>
              <div className="faq-body">
                <p>{item.a}</p>
              </div>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}

export default FAQ
